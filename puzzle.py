def columns(board: list) -> list[list]:
    """
    Return a list of columns.
    >>> board = [\
 "**** ****",\
 "***1 ****",\
 "**  3****",\
 "* 4 1****",\
 "     9 5 ",\
 " 6  83  *",\
 "3   1  **",\
 "  8  2***",\
 "  2  ****"\
]
    >>> columns(board)
    [['*', '*', '*', '*', ' ', ' ', '3', ' ', ' '], ['*', '*', '*', ' ', ' ', '6', ' ', ' ', ' '], \
['*', '*', ' ', '4', ' ', ' ', ' ', '8', '2'], ['*', '1', ' ', ' ', ' ', ' ', ' ', ' ', ' '], \
[' ', ' ', '3', '1', ' ', '8', '1', ' ', ' '], ['*', '*', '*', '*', '9', '3', ' ', '2', '*'], \
['*', '*', '*', '*', ' ', ' ', ' ', '*', '*'], ['*', '*', '*', '*', '5', ' ', '*', '*', '*'], \
['*', '*', '*', '*', ' ', '*', '*', '*', '*']]
    """
    cols = []
    for i in range(9):
        cols.append([n[i] for n in board])
    return cols
